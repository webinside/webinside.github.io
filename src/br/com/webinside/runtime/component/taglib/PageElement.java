package br.com.webinside.runtime.component.taglib;

import javax.servlet.http.HttpServletResponse;
import javax.servlet.jsp.JspException;
import javax.servlet.jsp.tagext.TagSupport;

import br.com.webinside.runtime.core.ExecuteParams;

/**
 * Classe que implementa um TagLib para um elemento da página.
 *
 * @author Geraldo Moraes
 * @version $Revision: 1.1 $
 */
public class PageElement extends TagSupport {

	private static final long serialVersionUID = 1L;
	private String type;
    private String name;
    private boolean exit;

    /**
     * Executa a função.
     *
     * @return a flag para não processar o body
     *
     * @throws JspException em caso de uma exceção jsp.
     */
    public int doStartTag() throws JspException {
    	exit = true;
        try {
            Object obj = pageContext.getRequest().getAttribute("wiParams");
            if (obj instanceof ExecuteParams) {
                exit = false;
            	Class cl = Class.forName("br.com.webinside.runtime.component." + getType());
            	pageContext.setAttribute(getName(), cl.getConstructor().newInstance());
            } else {
                HttpServletResponse response =
                    (HttpServletResponse) pageContext.getResponse();
                (response).sendError(HttpServletResponse.SC_FORBIDDEN);
            }
        } catch (Exception err) {
        	throw new JspException(err);
        }
        return SKIP_BODY;
    }
    
    /**
     * Finaliza a tag.
     *
     * @return se a página deve ser processada.
     *
     * @throws JspException em caso de uma exceção jsp.
     */
    public int doEndTag() throws JspException {
        return ((exit) ? SKIP_PAGE
                       : EVAL_PAGE);
    }

    /**
     * Name do elemento da página.
     *
     * @return o nome do elemento da página.
     */
    public String getName() {
        return name;
    }

    /**
     * Name do elemento da página.
     *
     * @param name indica o id do elemento da página.
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Tipo de elemento da página.
     *
     * @return o tipo de elemento da página.
     */
    public String getType() {
        return type;
    }

    /**
     * Tipo de elemento da página.
     *
     * @param type indica o tipo de elemento da página.
     */
    public void setType(String type) {
        this.type = type;
    }
}
